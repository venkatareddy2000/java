package com.java.multithreading.synchronizedblock;
/*
 * A synchronized block in Java is a section of code that is enclosed within a
 * block and synchronized using a specified object (also called a monitor) as a
 * synchronization lock. Synchronized blocks are used to ensure that only one
 * thread at a time can execute the code inside the synchronized block, which
 * helps prevent data races and maintain data consistency when multiple threads
 * are accessing shared resources.
 */