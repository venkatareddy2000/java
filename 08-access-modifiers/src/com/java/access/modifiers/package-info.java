package com.java.access.modifiers;

/*
 * Access modifiers are object-oriented programming that is used to set the
 * accessibility of classes, constructors, methods, and other members of Java.
 * 
 * There are four kind of access modifiers.
 * 
 * 1.public : We can access public classes, constructors, methods, and other
 * members of Java in any other classes present inside and outside of different
 * packages.
 * 
 * 2.private : We can access private classes, constructors,methods, and other
 * members of Java with in that class.
 * 
 * 3.protected : We can access protected classes, constructors, methods, and
 * other members of Java in other classes that is present inside the same
 * package without inheritance and other package classes by inheritance.
 * 
 * 4.default : We can access default classes, constructors, methods, and other
 * members of Java in classes that is present inside the same package.
 */