package com.java.flowcontrol.selectionstatement;

public class IfStatement {

	public static void main(String[] args) {
		
		/*
		 * If statement : If the condition inside the expression is true, then
		 * statements inside the If block will be executed Otherwise the statement after
		 * If block will be executed
		 */

		int x = 10;
		if (x > 9) {
			System.out.println("The number is greater than 9.");
		}

	}

}
