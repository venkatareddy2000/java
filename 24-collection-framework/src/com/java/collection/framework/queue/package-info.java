package com.java.collection.framework.queue;
/*
 * A queue is a linear data structure in computer science that follows the
 * First-In-First-Out (FIFO) principle. In a queue, elements are inserted at one
 * end, called the rear or tail, and removed from the other end, called the
 * front or head. Think of it like a line of people waiting for something; the
 * person who arrives first (enters the queue) is the first to be served and
 * leaves the queue first.
 * 
 * Here are key characteristics and concepts related to queues:
 * 
 * Operations:
 * 
 * Enqueue: Adding an element to the rear of the queue.
 * 
 * Dequeue: Removing an element from the front of the queue.
 * 
 * Peek or Front: Examining the element at the front of the queue without
 * removing it.
 * 
 * isEmpty: Checking if the queue is empty.
 * 
 * Size: Determining the number of elements in the queue.
 * 
 * Types of Queues:
 * 
 * Linear Queue: A simple queue with a fixed size. Once it's full, no more
 * elements can be added until some are removed (overflow condition).
 * 
 * Circular Queue: A variation of the linear queue where the rear wraps around
 * to the front when the end of the queue is reached. It effectively reuses
 * empty slots, preventing overflow.
 * 
 * Priority Queue: A specialized queue where elements have priorities assigned
 * to them. Elements with higher priority are dequeued before those with lower
 * priority.
 * 
 * Double-ended Queue (Deque): Allows insertion and deletion at both ends, the
 * front and the rear.
 */