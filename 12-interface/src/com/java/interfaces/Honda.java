package com.java.interfaces;

public interface Honda {
	
	/*
	 * To create an interface we need to use the interface keyword By default all
	 * the methods present in the interface is public and abstract, so we no need to
	 * mention explicitly.
	 */

	/*
	 * By default the variables we define in interfaces are public static final and
	 * we cannot change the values of these variables, as it will be constant
	 * throughout the program.
	 */
	void go();

	void run();

}
