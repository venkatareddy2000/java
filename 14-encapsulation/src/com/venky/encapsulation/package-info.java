package com.venky.encapsulation;

/*
 * Encapsulation is one of the fundamental concepts in object-oriented
 * programming (OOP), and it is also a principle of Java programming. It refers
 * to the bundling of data (attributes) and the methods (functions) that operate
 * on the data into a single unit known as a class. The purpose of encapsulation
 * is to hide the internal details of an object and provide a well-defined
 * interface for interacting with it.
 * 
 * In Java, encapsulation is achieved through the use of access modifiers and
 * getter and setter methods:
 * 
 * Access Modifiers: Java provides four types of access modifiers to control the
 * visibility of class members (fields and methods):
 * 
 * private: The member is only accessible within the same class. default (no
 * modifier): The member is accessible within the same package. protected: The
 * member is accessible within the same package and by subclasses. public: The
 * member is accessible from anywhere. Getter and Setter Methods: These methods
 * allow controlled access to the private fields of a class. They enable reading
 * (getter) and modifying (setter) the values of private fields while enforcing
 * any necessary validation or logic.
 */