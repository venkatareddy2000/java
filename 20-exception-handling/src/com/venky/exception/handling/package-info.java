package com.venky.exception.handling;

/*
 * An exception, in the context of computer programming, refers to an unexpected
 * event or condition that disrupts the normal flow of a program's execution.
 * Exceptions are usually caused by errors or exceptional situations that occur
 * during runtime. Instead of allowing the program to crash or produce incorrect
 * results, exceptions provide a mechanism to handle these situations
 * gracefully.
 * 
 * When an exception occurs, the program can "throw" or "raise" the exception,
 * which essentially means generating a signal that something went wrong. The
 * program then searches for an appropriate "exception handler" or "catch block"
 * that can effectively deal with the specific type of exception that was
 * thrown. The catch block contains code to manage the exceptional situation and
 * can include error messages, corrective actions, or even termination
 * procedures.
 * 
 * In the process of compiling and executing a program we see three types of
 * errors.
 * 
 * 1. Compile time errors : If we do not follow the syntactical rules that a
 * particular programming language compiler defines, then we see compile time
 * errors.
 * 
 * 2. Logical errors: Logical errors happen if we as a programmer do some
 * mistakes and the expected output will be different from what we intended it
 * to be.
 * 
 * 3. Run time errors: These happen at runtime because of several reasons and
 * these will cause our program to terminate abruptly.And the end user will see
 * an unfriendly or informal message which he can't make sense out of.
 */